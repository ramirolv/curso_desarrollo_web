def sumar(num1, num2):
    return num1 + num2

def restar(num1, num2):
    return num1 - num2

def dividir(num1, num2):
    return num1 / num2

def multiplicar(num1, num2):
    return num1 * num2

v1 = 10
v2 = 15

res_suma = sumar(v1, v2)
res_resta = restar(v1, v2)
res_division = dividir(v1, v2)
res_multi = multiplicar(v1, v2)

print(res_suma)
print(res_resta)
print(res_division)
print(res_multi)